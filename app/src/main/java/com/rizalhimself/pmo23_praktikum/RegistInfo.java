package com.rizalhimself.pmo23_praktikum;

public class RegistInfo {
    private String mhsNim;
    private String mhsNama;
    private String mhsEmail;

    public RegistInfo() {

    }

    public String getMhsNama() {
        return mhsNama;
    }

    public void setMhsNama(String mhsNama) {
        this.mhsNama = mhsNama;
    }

    public String getMhsNim() {
        return mhsNim;
    }

    public void setMhsNim(String mhsNim) {
        this.mhsNim = mhsNim;
    }

    public String getMhsEmail() {
        return mhsEmail;
    }

    public void setMhsEmail(String mhsEmail) {
        this.mhsEmail = mhsEmail;
    }

}
